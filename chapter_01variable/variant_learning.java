/*
 * Java is a statically typed language.
 * This means you must declare the type of every variable before using it.(int a = 1;)
 */
public class variant_learning{

	public static void main(String[] args){

		int c;//declare a variable
		
		int a = 1;//defind a variant "a" ,integer type the name is "a",the value is "1"
		
		int b = 3;//defind a variant "b" ,integer type the name is "b",the value is "2"
		
		b = 89;//Assign the value 89 to b
		
		System.out.println("a");//print a 
		
		System.out.println("b");//print b
		
		System.out.println(a);//print 1 
		
		System.out.println(b);//print 89
	}
}
